def generate_search_template(page_data):
    """Generate search.liquid based on extracted search page data"""
    sd = page_data.get("search_data", {})
    
    has_predictive = sd.get("has_predictive_search", True)
    
    template = '''<!-- search.liquid — Generated by Shopify Recon -->
<div class="search-page">
  
  <h1>Search</h1>
  
  <form action="/search" method="get" role="search" class="search-form">
    <input type="search" 
           name="q" 
           value="{{ search.terms | escape }}"
           placeholder="Search products..."
           class="search-input"
           {% if has_predictive %}data-predictive-search{% endif %}>
    
    <select name="type" class="search-type">
      <option value="product" {% if search.types contains 'product' %}selected{% endif %}>Products</option>
      <option value="article" {% if search.types contains 'article' %}selected{% endif %}>Articles</option>
      <option value="page" {% if search.types contains 'page' %}selected{% endif %}>Pages</option>
    </select>
    
    <button type="submit" class="btn btn-search">Search</button>
  </form>

  {% if search.performed %}
    {% if search.results.size > 0 %}
      <p class="search-results-count">
        {{ search.results_count }} result{{ search.results_count | pluralize }} for "{{ search.terms }}"
      </p>
      
      <div class="search-results">
        {% for item in search.results %}
          {% if item.object_type == 'product' %}
            <div class="search-result product-result">
              {% render 'product-card', product: item %}
            </div>
          {% elsif item.object_type == 'article' %}
            <div class="search-result article-result">
              <a href="{{ item.url }}">
                <h3>{{ item.title }}</h3>
                <p>{{ item.content | strip_html | truncatewords: 30 }}</p>
              </a>
            </div>
          {% elsif item.object_type == 'page' %}
            <div class="search-result page-result">
              <a href="{{ item.url }}">
                <h3>{{ item.title }}</h3>
                <p>{{ item.content | strip_html | truncatewords: 30 }}</p>
              </a>
            </div>
          {% endif %}
        {% endfor %}
      </div>
      
      {% if paginate.pages > 1 %}
        <nav class="pagination">
          {% if paginate.previous %}
            <a href="{{ paginate.previous.url }}">&laquo; Previous</a>
          {% endif %}
          {% for part in paginate.parts %}
            {% if part.is_link %}
              <a href="{{ part.url }}">{{ part.title }}</a>
            {% else %}
              <span class="current">{{ part.title }}</span>
            {% endif %}
          {% endfor %}
          {% if paginate.next %}
            <a href="{{ paginate.next.url }}">Next &raquo;</a>
          {% endif %}
        </nav>
      {% endif %}
      
    {% else %}
      <div class="search-no-results">
        <p>No results found for "{{ search.terms }}".</p>
        <p>Try different keywords or check spelling.</p>
        
        <h3>Popular Searches</h3>
        <div class="popular-searches">
          <a href="/search?q=new+arrivals">New Arrivals</a>
          <a href="/search?q=best+sellers">Best Sellers</a>
          <a href="/search?q=sale">Sale</a>
        </div>
      </div>
    {% endif %}
  {% endif %}
</div>

{% if has_predictive %}
<script>
  // Predictive search
  var searchInput = document.querySelector('[data-predictive-search]');
  if (searchInput) {
    var debounceTimer;
    searchInput.addEventListener('input', function() {
      clearTimeout(debounceTimer);
      debounceTimer = setTimeout(function() {
        var query = searchInput.value;
        if (query.length < 2) return;
        
        fetch('/search/suggest.json?q=' + encodeURIComponent(query) + '&resources[type]=product')
          .then(r => r.json())
          .then(data => {
            var products = data.resources.results.products || [];
            showSearchSuggestions(products);
          })
          .catch(err => console.error('Search error:', err));
      }, 300);
    });
  }
  
  function showSearchSuggestions(products) {
    var existing = document.getElementById('SearchSuggestions');
    if (existing) existing.remove();
    
    if (products.length === 0) return;
    
    var div = document.createElement('div');
    div.id = 'SearchSuggestions';
    div.className = 'search-suggestions';
    
    products.slice(0, 5).forEach(function(p) {
      var a = document.createElement('a');
      a.href = '/products/' + p.handle;
      a.innerHTML = '<img src="' + p.image + '" alt="' + p.title + '" width="50">' +
                     '<span>' + p.title + '</span>' +
                     '<span class="price">' + p.price + '</span>';
      div.appendChild(a);
    });
    
    searchInput.parentNode.appendChild(div);
  }
</script>
{% endif %}
'''
    return template


def generate_cart_template(page_data):
    """Generate cart.liquid based on extracted cart page data"""
    cd = page_data.get("cart_data", {})
    
    has_qty = cd.get("has_quantity_selector", True)
    has_remove = cd.get("has_remove_button", True)
    has_notes = cd.get("has_cart_note", True)
    has_discount = cd.get("has_discount_field", False)
    has_checkout = cd.get("has_checkout_button", True)
    has_empty_msg = cd.get("has_empty_message", True)
    
    # Build cart template without f-string to avoid Liquid {%%} conflicts
    qty_section = ""
    if has_qty:
        qty_section = '''            <div class="cart-item-quantity">
              <button type="button" onclick="updateQty({{ forloop.index }}, -1)">-</button>
              <input type="number" name="updates[]" value="{{ item.quantity }}" min="0"
                     onchange="updateQty({{ forloop.index }}, this.value)">
              <button type="button" onclick="updateQty({{ forloop.index }}, 1)">+</button>
            </div>'''
    
    remove_section = ""
    if has_remove:
        remove_section = '''            <div class="cart-item-remove">
              <a href="/cart/change?line={{ forloop.index }}&quantity=0"
                 class="remove-link">Remove</a>
            </div>'''
    
    notes_section = ""
    if has_notes:
        notes_section = '''        <div class="cart-note">
          <label for="CartNote">Order Note</label>
          <textarea name="note" id="CartNote" placeholder="Special instructions...">
            {{ cart.note }}
          </textarea>
        </div>'''
    
    discount_section = ""
    if has_discount:
        discount_section = '''        <div class="cart-discount">
          <label for="CartDiscount">Discount Code</label>
          <input type="text" id="CartDiscount" placeholder="Enter code">
          <button type="button" onclick="applyDiscount()">Apply</button>
        </div>'''
    
    checkout_section = ""
    if has_checkout:
        checkout_section = '''        <div class="cart-actions">
          <a href="/collections/all" class="btn btn-continue">Continue Shopping</a>
          <button type="submit" name="checkout" class="btn btn-checkout">
            Checkout • {{ cart.total_price | money }}
          </button>
        </div>'''
    
    empty_section = ""
    if has_empty_msg:
        empty_section = '''    <div class="cart-empty">
      <h2>Your cart is empty</h2>
      <p>Looks like you haven't added anything yet.</p>
      <a href="/collections/all" class="btn btn-shop">Start Shopping</a>
    </div>'''
    
    template = '''<!-- cart.liquid — Generated by Shopify Recon -->
<div class="cart-page">
  
  <h1>Shopping Cart</h1>
  
  {% if cart.item_count > 0 %}
    
    <form action="/cart" method="post" class="cart-form">
      
      <div class="cart-items">
        {% for item in cart.items %}
          <div class="cart-item" data-line="{{ forloop.index }}">
            <div class="cart-item-image">
              <a href="{{ item.url }}">
                {% if item.image %}
                  <img src="{{ item.image | image_url: width: 150 }}"
                       alt="{{ item.title | escape }}"
                       width="150" height="150"
                       loading="lazy">
                {% endif %}
              </a>
            </div>
            
            <div class="cart-item-info">
              <a href="{{ item.url }}" class="cart-item-title">
                {{ item.product.title }}
              </a>
              
              {% unless item.variant.title contains 'Default' %}
                <p class="cart-item-variant">{{ item.variant.title }}</p>
              {% endunless %}
              
              <p class="cart-item-vendor">{{ item.vendor }}</p>
              
              <p class="cart-item-price">
                {{ item.final_line_price | money }}
                {% if item.original_line_price != item.final_line_price %}
                  <s>{{ item.original_line_price | money }}</s>
                {% endif %}
              </p>
            </div>
            
            ''' + qty_section + '''
            
            ''' + remove_section + '''
          </div>
        {% endfor %}
      </div>
      
      <div class="cart-footer">
        
        ''' + notes_section + '''
        
        ''' + discount_section + '''
        
        <div class="cart-totals">
          <div class="cart-subtotal">
            <span class="label">Subtotal</span>
            <span class="value">{{ cart.total_price | money }}</span>
          </div>
          
          {% if cart.total_discounts > 0 %}
            <div class="cart-discount-amount">
              <span class="label">Discount</span>
              <span class="value">-{{ cart.total_discounts | money }}</span>
            </div>
          {% endif %}
          
          <div class="cart-shipping-note">
            Shipping and taxes calculated at checkout
          </div>
        </div>
        
        ''' + checkout_section + '''
      </div>
    </form>
    
  {% else %}
    
    ''' + empty_section + '''
    
  {% endif %}
</div>

<script>
  function updateQty(line, qty) {
    var newQty = typeof qty === 'number' 
      ? parseInt(document.querySelector('[data-line="' + line + '"] input').value) + qty
      : parseInt(qty);
    
    if (newQty < 0) newQty = 0;
    
    fetch('/cart/change.js', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ line: line, quantity: newQty })
    })
    .then(r => r.json())
    .then(cart => {
      if (newQty === 0) {
        document.querySelector('[data-line="' + line + '"]').remove();
      } else {
        document.querySelector('[data-line="' + line + '"] input').value = newQty;
      }
      updateCartTotals(cart);
    });
  }
  
  function updateCartTotals(cart) {
    document.querySelector('.cart-subtotal .value').textContent = formatMoney(cart.total_price);
    if (cart.item_count === 0) {
      location.reload();
    }
  }
  
  function formatMoney(cents) {
    return '$' + (cents / 100).toFixed(2);
  }
</script>
'''
    return template


