def main():
    if len(sys.argv) < 3:
        print("🏗️ FULL TEMPLATE GENERATOR")
        print("━" * 69)
        print()
        print("Usage: python3 template-generator.py <page-analysis.json> <output-dir>")
        print("Example: python3 template-generator.py ./page-analysis.json ./my-theme")
        sys.exit(1)
    
    analysis_path = sys.argv[1]
    output_dir = sys.argv[2]
    
    print("🏗️ FULL TEMPLATE GENERATOR")
    print("━" * 69)
    print(f"  Input: {analysis_path}")
    print(f"  Output: {output_dir}")
    print()
    
    analysis = load_analysis(analysis_path)
    pages = analysis.get("pages", {})
    
    # Create directory structure
    dirs = ["templates", "templates/customers", "snippets", "layout", "sections", "assets", "config"]
    for d in dirs:
        os.makedirs(os.path.join(output_dir, d), exist_ok=True)
    
    generated = []
    
    # Layout
    print("  [1/4] Generating layout...")
    layout_path = os.path.join(output_dir, "layout", "theme.liquid")
    with open(layout_path, "w") as f:
        f.write(generate_layout(pages))
    generated.append("layout/theme.liquid")
    
    # Templates
    print("  [2/4] Generating templates...")
    templates = {
        "product.liquid": generate_product_template(pages.get("product", {})),
        "collection.liquid": generate_collection_template(pages.get("collection", {})),
        "search.liquid": generate_search_template(pages.get("search", {})),
        "cart.liquid": generate_cart_template(pages.get("cart", {})),
        "404.liquid": generate_404_template(),
        "page.liquid": generate_page_template(),
        "blog.liquid": generate_blog_template(),
        "article.liquid": generate_article_template(),
        "list-collections.liquid": generate_list_collections_template(),
    }
    
    for name, content in templates.items():
        path = os.path.join(output_dir, "templates", name)
        with open(path, "w") as f:
            f.write(content)
        generated.append(f"templates/{name}")
    
    # Customer templates
    print("  [3/4] Generating customer templates...")
    customer_templates = generate_customers_templates()
    for name, content in customer_templates.items():
        path = os.path.join(output_dir, "templates", name)
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w") as f:
            f.write(content)
        generated.append(f"templates/{name}")
    
    # Snippets
    print("  [4/4] Generating snippets...")
    snippets = generate_snippets()
    for name, content in snippets.items():
        path = os.path.join(output_dir, "snippets", name)
        with open(path, "w") as f:
            f.write(content)
        generated.append(f"snippets/{name}")
    
    # Summary
    print(f"\n✅ TEMPLATE GENERATION COMPLETE")
    print(f"━" * 69)
    print(f"  📁 Output: {output_dir}/")
    print(f"  📄 {len(generated)} files generated")
    print()
    print("📂 Theme Structure:")
    for f in generated:
        print(f"  • {f}")
    
    print(f"\n🚀 Deploy:")
    print(f"  shopify theme dev --store=YOUR_STORE.myshopify.com --path={output_dir}")
    print(f"  shopify theme push --store=YOUR_STORE.myshopify.com --path={output_dir}")


if __name__ == "__main__":
    main()
