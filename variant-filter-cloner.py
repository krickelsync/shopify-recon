    picker_type = "dropdown"  # default
    if product_soup:
        if product_soup.find('select', attrs={'name': re.compile(r'id|variant', re.I)}):
            picker_type = "dropdown"
        elif product_soup.find(class_=re.compile(r'variant.*radio|swatch|option.*button', re.I)):
            picker_type = "swatch"
        elif product_soup.find('input', attrs={'type': 'radio', 'name': re.compile(r'option|variant', re.I)}):
            picker_type = "radio"
    
    print(f"Picker type detected: {picker_type}")
    
    # Load product data for variant structure
    variants_example = []
    if products_json_path.exists():
        data = json.load(open(products_json_path))
        products = data.get('products', data) if isinstance(data, dict) else data
        if products and isinstance(products, list):
            first = products[0]
            variants_example = first.get('variants', [])
            print(f"Example product: {first.get('title', 'N/A')}")
            print(f"Variants: {len(variants_example)}")
            if variants_example:
                v = variants_example[0]
                v_title = v.get('title', 'N/A')
                v_price = v.get('price', 0)
                try:
                    v_price_float = float(v_price) / 100
                except (TypeError, ValueError):
                    v_price_float = 0
                print(f"  Example: {v_title} - ${v_price_float:.2f}")
    
    # Generate main-product.liquid
    sections_dir = extracted_dir / "theme" / "sections"
    sections_dir.mkdir(parents=True, exist_ok=True)
    
