def copy_theme_structure(theme_dir, output_dir):
    """Copy entire theme structure to output"""
    import shutil
    
    # Copy everything first
    if os.path.exists(output_dir):
        shutil.rmtree(output_dir)
    shutil.copytree(theme_dir, output_dir)
    print(f"  ✅ Theme structure copied to {output_dir}")


