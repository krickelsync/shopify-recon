def inject_css_into_layout(theme_dir, theme_css, animations_css, output_dir):
    """Inject CSS into theme.liquid layout"""
    
    # Read theme.liquid
    layout_path = os.path.join(theme_dir, "layout", "theme.liquid")
    if not os.path.exists(layout_path):
        print(f"❌ No layout/theme.liquid found in {theme_dir}")
        return False
    
    with open(layout_path) as f:
        layout = f.read()
    
    # Build injected CSS block
    injected_css = f"""
<!-- ═══ Injected by Shopify Recon CSS Injector ═══ -->
<style>
/* Theme CSS — Extracted from competitor store */
{theme_css}

/* Animation CSS — Extracted hover effects, transitions, keyframes */
{animations_css}
</style>
<!-- ═══ End Injected CSS ═══ -->
"""
    
    # Find {{ content_for_header }} and inject after it
    if "{{ content_for_header }}" in layout:
        layout = layout.replace(
            "{{ content_for_header }}",
            "{{ content_for_header }}\n" + injected_css
        )
        print("  ✅ CSS injected after content_for_header")
    elif "</head>" in layout:
        layout = layout.replace("</head>", injected_css + "\n</head>")
        print("  ✅ CSS injected before </head>")
    else:
        # Fallback: inject at the beginning
        layout = injected_css + "\n" + layout
        print("  ⚠️  CSS injected at beginning (no head tag found)")
    
    # Also add CSS asset references for production use
    asset_ref = """
<!-- CSS asset references (for production deployment) -->
<link rel="stylesheet" href="{{ 'theme.css' | asset_url }}">
<link rel="stylesheet" href="{{ 'animations.css' | asset_url }}">
"""
    
    if "{{ 'theme.css' | asset_url }}" not in layout:
        if "</head>" in layout:
            layout = layout.replace("</head>", asset_ref + "\n</head>")
    
    # Write output
    os.makedirs(os.path.join(output_dir, "layout"), exist_ok=True)
    output_layout = os.path.join(output_dir, "layout", "theme.liquid")
    with open(output_layout, "w") as f:
        f.write(layout)
    
    return True


