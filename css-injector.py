    templates_dir = os.path.join(theme_dir, "templates")
    if not os.path.exists(templates_dir):
        return
    
    output_templates = os.path.join(output_dir, "templates")
    os.makedirs(output_templates, exist_ok=True)
    
    # Copy all template files
    for root, dirs, files in os.walk(templates_dir):
        for f in files:
            src = os.path.join(root, f)
            rel_path = os.path.relpath(src, templates_dir)
            dst = os.path.join(output_templates, rel_path)
            os.makedirs(os.path.dirname(dst), exist_ok=True)
            
            with open(src) as fh:
                content = fh.read()
            
